package trabalhoav1;

/**
 * Classe Pessoa
 * @author Rafael
 * @version 1.0
 */
public class Pessoa {
	
	private String nome;
	private int idade;
	private String sexo;
	
	/* Getters and Setters */
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String getSexo() {
		return sexo;
	}

	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
}